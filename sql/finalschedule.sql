DROP VIEW IF EXISTS `gc_schedule_check_statistics_v`;
CREATE ALGORITHM=UNDEFINED DEFINER=`dlwy`@`%` SQL SECURITY DEFINER  VIEW `gc_schedule_check_statistics_v` AS SELECT * FROM gc_schedule_check_v
UNION 
SELECT * FROM gc_schedule_scheduler_check_v ;

-- ----------------------------
-- View structure for gc_schedule_check_v
-- ----------------------------
DROP VIEW IF EXISTS `gc_schedule_check_v`;
CREATE ALGORITHM=UNDEFINED DEFINER=`dlwy`@`%`  VIEW `gc_schedule_check_v` AS SELECT
		t2.name,
		t1.pid,
		DATE_FORMAT(t1.checkTime, '%Y-%m-%d') AS checkTime,
		t1.checkStatus
	FROM
		gc_schedule_check t1
	INNER JOIN gc_schedule_person t2 ON (
		t1.pid = t2.pid
	)  ORDER BY pid ;

-- ----------------------------
-- View structure for gc_schedule_group_person_v
-- ----------------------------
DROP VIEW IF EXISTS `gc_schedule_group_person_v`;
CREATE ALGORITHM=UNDEFINED DEFINER=`dlwy`@`%` SQL SECURITY DEFINER  VIEW `gc_schedule_group_person_v` AS SELECT
	t1.pid,
	t1.name,
	t2.groupName,
	t2.gid
FROM
	gc_schedule_person t1
LEFT JOIN gc_schedule_group t2 ON t2.groupItem LIKE concat('%', t1.`name`, '%') ;

-- ----------------------------
-- View structure for gc_schedule_scheduler_check_v
-- ----------------------------
DROP VIEW IF EXISTS `gc_schedule_scheduler_check_v`;
CREATE ALGORITHM=UNDEFINED DEFINER=`dlwy`@`%`  VIEW `gc_schedule_scheduler_check_v` AS SELECT
	t3.name,
	t3.pid,
	t3.day AS checkTime,
	t3.events AS checkStatus
FROM
	gc_schedule_scheduler t3
where pid in (select pid from gc_schedule_check_v) and t3.day not in (select checkTime from gc_schedule_check_v)
 ORDER BY pid
